#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*temp_s;

	temp_s = (char *)s;
	if (n == 0)
		return ;
	while (n--)
	{
		*temp_s = 0;
		temp_s++;
	}
}
